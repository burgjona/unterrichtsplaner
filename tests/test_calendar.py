"""M4 über die API: Auto-Kalendereintrag, Ferien/Feiertage (gemockt), Jahresplanung."""


def test_lesson_date_syncs_auto_calendar_entry(client, auth):
    l = client.post("/api/lessons", json={"title": "Balladen", "subject": "Deutsch", "date": "2025-09-15"}).json()
    autos = [c for c in client.get("/api/calendar").json() if c["lessonId"] == l["id"]]
    assert len(autos) == 1 and autos[0]["entryDate"] == "2025-09-15" and autos[0]["autoGenerated"] is True

    client.put(f"/api/lessons/{l['id']}", json={"date": "2025-09-22"})
    autos = [c for c in client.get("/api/calendar").json() if c["lessonId"] == l["id"]]
    assert autos[0]["entryDate"] == "2025-09-22"  # zieht mit

    client.put(f"/api/lessons/{l['id']}", json={"date": None})
    assert [c for c in client.get("/api/calendar").json() if c["lessonId"] == l["id"]] == []  # weg


def test_lesson_delete_removes_auto_entry(client, auth):
    l = client.post("/api/lessons", json={"title": "X", "subject": "WTH", "date": "2025-09-10"}).json()
    assert any(c["lessonId"] == l["id"] for c in client.get("/api/calendar").json())
    client.delete(f"/api/lessons/{l['id']}")
    assert not any(c.get("lessonId") == l["id"] for c in client.get("/api/calendar").json())


