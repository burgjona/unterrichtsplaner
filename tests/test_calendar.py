def test_planning_preview(client, auth):
    sy = client.post("/api/school-years", json={"label": "2025/2026", "startDate": "2025-08-11", "endDate": "2026-06-30"}).json()
    cls = client.post("/api/classes", json={"name": "8a", "subject": "Deutsch", "grade": 8, "track": "RS", "weeklyHours": 3}).json()
    for i, (code, ustd) in enumerate([("LB1", 15), ("LB2", 22)], 1):
        client.post("/api/lernbereiche", json={"subject": "Deutsch", "grade": 8, "track": "RS",
                                               "code": code, "title": f"Bereich {code}", "richtwertUstd": ustd, "sortOrder": i})
    res = client.post("/api/planning/preview", json={"schoolYearId": sy["id"], "classId": cls["id"]}).json()
    assert res["teachingWeeks"] > 0
    assert res["planned"] == 2
    assert res["blocks"][0]["code"] == "LB1"
    assert res["blocks"][0]["endDate"] < res["blocks"][1]["startDate"]
